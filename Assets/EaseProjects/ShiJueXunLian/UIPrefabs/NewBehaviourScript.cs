1.软件基本功能要求:
1.1.运行条件：要求电脑使用Windows 7及以上系统、8GB或以上运行内存、2G以上独立显卡。通过webgl主流浏览器运行。
1.2.软件架构：B/S架构，支持互联网或局域网访问。
1.3.网络要求：软件不限制连接数，同时在线人数无上限要求。
1.4.研发技术：软件学生端采用3D技术开发。
2.主界面功能介绍:
2.1.界面实时显示实验名称、实验进度、当前得分和功能界面。
2.2.隐藏/显示功能：可以对主界面的UI界面进行隐藏/显示切换。
2.3.设置：打开设置界面可进行以下设置。
2.3.1.设置声音大小，包括环境音、音乐、音效、语音等。
2.3.2.设置项目运行质量，用户可结合当前使用的电脑硬件条件选择不同画质。可选择的级别有极速、标准、高清、完美。
2.3.3.可一键恢复默认设置。
2.4.提示：点击“提示”按钮会给出对应提示，提示类型有物品闪烁提示、文字提示或数字提示。
2.5.帮助：此功能主要是讲解软件的基本操作方法、交互操作方法和漫游操作方法。
2.6.上一步：点击后可回到上一步的操作。
2.7.暂停：激活后当前步骤结束时将进入暂停状态。此时不会自动进入下一步。
2.8.下一步：点击直接进入下一步。
2.9.步骤管理功能：实验分为大步骤和小步骤组成。界面实时显示当前大步骤和当前小步骤。
2.9.1.点击大步骤或者小步骤可弹出对应步骤列表。
2.9.2.点击主要步骤可展开显示该步骤下的小步骤。
2.9.3.步骤菜单中实时显示完成情况包括“已完成”、“未完成”、“进行中”，分别用不同颜色标记。
2.9.4.步骤跳转功能：可从步骤列表中选择任意步骤进行跳转。
2.10.文字提示框：主要提示的文字包括引导操作提示、知识点讲解等。
3.其它功能介绍:
3.1.选择题功能：项目中穿插选择题。当操作错误超过一定次数，会给出正确答案和问题解析。
3.2.常用交互形式：鼠标移动到可交互物体上时，物体会出现边缘光且显示物体名称。可使用点击、拖拽等形式进行交互。
3.3.知识点表现形式：用文字、3D模型、图片、选择题、视频、动画、等画面表现形式，结合鼠标点击、键盘输入等交互方式模拟整个实验流程。
3.4.刻刀雕刻后高亮显示刻痕提示，可以打开/关闭此提示。
3.5.雕刻完成后可旋转观察牙齿模型。
4.软件分为“教学模式”和“考核模式”可自行选择:
4.1.教学模式：
4.1.1.以教学讲解为目的，包含有详细的知识点说明和操作提示说明，部分知识点配有原理视频解说。
4.1.2.使用提示功能时不扣除分值。
4.1.3.可随时返回上一步或跳过当前步骤直接进入下一步。
4.1.4.可在步骤列表中进行任意步骤跳转。
4.1.5.教学模式下也能在界面上实时显示当前得分。
4.2.考核模式：
4.2.1.以验证学习效果为目的，实验中关键考核点提示会被隐藏。
4.2.2.使用提示功能时，会扣掉当前步骤得分并给出正确答案提示。
4.2.3.考核模式无法使用上一步跳转功能。
4.2.4.使用下一步跳转功能时当前步骤不得分。
4.2.5.考核模式下在步骤列表中无法进行步骤跳转。
4.2.6.考核模式退出实验时将自动统计成绩并提交至平台管理系统。
5.实验内容介绍：
进入后可选择要雕刻的牙齿种类，切牙、尖牙和磨牙。
5.1.实验准备
5.1.1.数据准备：展示画线数据参考，包含牙体全长、冠长、根长、冠宽、颈宽、冠厚、颈厚、近中颈曲度、远中颈曲度等详细数据以及切端面、唇面、舌面、近中面、远中面的形态数据图。
5.1.2.物品准备：包含牙齿雕刻所需要的所有工具、蜡块、刻度尺、三角刀、雕刻刀等。
5.2.外形设计
5.2.1.画中线、画冠根分界线。
5.2.2.分别确定唇侧面、近中面、舌侧面、远中面
5.2.3.分别对唇侧面、近中面、舌侧面、远中面、牙冠面进行标点划线
5.2.4.分别画出唇侧面、近中面、舌侧面、远中面、牙冠面外形
5.2.5.外形展示：自由旋转观察画出的外形标记。
5.3.牙齿雏形雕刻：初步雕刻外形。
5.3.1.分别雕刻唇侧面、近中面、舌侧面、远中面。
5.4.初步圆钝成型
5.4.1.初步轴面圆钝
5.4.2.精修轴面圆钝
5.5.精修细节：精修颈曲度、舌侧窝、牙根，然后抛光。
5.6.结构测量判定
5.6.1.结构展示：自由旋转观察雕刻完成的牙齿模型。
